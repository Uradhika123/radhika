class Switchcase
{
public static void main(String[] args)
{
int no=2;
switch(no){
case 1:System.out.println("FCD");
       break;
case 2:System.out.println("FC");
       break;
case 3:System.out.println("SC");
       break;
case 4:System.out.println("PASS");
       break;
default:System.out.println("Invalid charecter");
       break;
}

}
}